// parseInt converts strings into numbers

console.log("ParseInt");
console.log(parseInt("42"))
console.log(parseInt("42px"))
console.log(parseInt("42pfdjljdsfjx"))
console.log(parseInt("afaj42px"))
console.log(parseInt("3.14"))

// parsefloat converts strings into float
console.log("ParseFloat");
console.log(parseFloat("42"))
console.log(parseFloat("42px"))
console.log(parseFloat("42pfdjljdsfjx"))
console.log(parseFloat("afaj42px"))
console.log(parseFloat("3.14"))

